public enum SVGSpecElement: String {

    public typealias RawValue = String

    case rect = "rect"
    case circle = "circle"
    case ellipse = "ellipse"
    case audio = "audio"
    case canvas = "canvas"
    case foreign = "foreignObject"
    case iframe = "iframe"
    case line = "line"
    case path = "path"
    case polygon = "polygon"
    case polyline = "polyline"
    case text = "text"
    case textPath = "textPath"
    case tspan = "tspan"
    case video = "video"

}
